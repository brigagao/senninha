          <h1 class="mb-5 mt-5">TRAJETÓRIA</h1>
          <p class="mb-5">Além das corridas de carros, Senna dedicava-se a jet skis, motos, aeromodelos e principalmente helicópteros. Ele também administrava diversas marcas e empreendimentos, além de ter patrocinado vários programas de assistência filantrópica, principalmente os ligados a crianças carentes</p>
