          <p class="mb-5 mt-5">Paulistano do bairro de Santana, Ayrton Senna nasceu com a velocidade correndo em suas veias. Incentivado pelo seu pai (Sr. Milton), aos 4 anos de idade já apresentava uma habilidade incrível com o Kart e, a partir disso, a paixão pelo barulho do motor acelerado só aumentou. Aos 9 anos, conduzia jipes com destreza característica de muita experiência; aos 13, já competia oficialmente. Terminou como segundo colocado diversas vezes, o que não o bastava. Foi quando, em 1977, pôde sentir pela primeira vez o saboroso gosto de uma vitória - e decidir que aquele era o seu lugar.
            Em 1981, começou a competir na Europa, ganhando o campeonato inglês de Fórmula Ford 1600 - com uma marca de 12 vitórias em 20 corridas. Foi nesse período que decidiu usar o sobrenome de solteira de sua mãe, Senna, já que Silva é um nome muito comum no Brasil. Em 1983, venceu o campeonato inglês de Fórmula 3 pela equipe Dick Bennets, com 13 vitórias em 21 corridas.
          </p>

          <p>Em 1983 alcançou o título de campeão do Campeonato Britânico de Fórmula 3 batendo vários recordes. Seu desempenho impulsionou sua ascensão à Fórmula 1, fazendo sua primeira aparição na categoria no Grande Prêmio do Brasil de 1984 pela equipe Toleman-Hart. Em sua primeira temporada, Senna pontuou em cinco corridas, fechando o ano com treze pontos e a nona posição na classificação geral dos pilotos. No ano seguinte, ingressou na Lotus-Renault, pela qual venceu seis grandes prêmios ao longo de três temporadas.</p>

          <p class="mb-5 mt-5">Em 1988, juntou-se ao francês Alain Prost na McLaren-Honda, com o qual teve grande rivalidade. Senna venceu oito etapas daquela temporada e sagrou-se campeão mundial pela primeira vez. Após a polêmica final de 1989 com Prost que resultou na segunda colocação do torneio, ele retomou o título em 1990, vencendo novamente na temporada seguinte, tornando-se o piloto mais jovem a conquistar um tricampeonato na Fórmula 1 até então. Em 1993, Senna foi vice-campeão, vencendo cinco corridas. Transferiu-se para a Williams em 1994, onde disputou apenas três etapas, a última sendo o Grande Prêmio de San Marino, onde se acidentou e acabou morrendo.</p>

          <a href="#"><img src="img/senhah61.png" class="img-fluid col-12" alt="Imagem responsiva" /></a>
          <p class="mt-5 mb-5">Pouco antes de sua morte, ele criou a estrutura de uma organização dedicada às crianças pobres brasileiras, que mais tarde se tornou o Instituto Ayrton Senna. Após sua morte, foi descoberto que ele havia doado em segredo uma porção muito grande de sua fortuna pessoal (estimada em cerca de US$ 400 milhões) para ajudar crianças pobres. </p>
