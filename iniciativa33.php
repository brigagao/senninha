                    <p class="texto-simples-instituto mt-5 mb-3">O Instituto Ayrton Senna e a Secretaria de Educação de São Paulo (SEDUC-SP) anunciam os resultados do monitoramento socioemocional de professores, realizado com os profissionais da rede estadual paulista entre maio e junho de 2021. Em formato
                        online, a participação foi aberta a todos os educadores da rede, com preenchimento voluntário, e recebeu as respostas de 42.789 professores da rede estadual, sendo 74,3% são do sexo feminino e 25,7% do sexo masculino. 64,0% atuam no Ensino
                        Fundamental Anos Finais (EFAF); 60,2% no Ensino Médio, 17,8% na Ensino Fundamental Anos Iniciais (EFAI) e 2,1% na Educação Infantil (EI).</p>

                    <p class="texto-simples-instituto">Os dados geram insumos para o entendimento da situação socioemocional dos docentes a partir de evidências. Entre outras informações, o mapeamento mostrou que a maior parte dos professores consideram Respeito e Justiça como as competências
                        mais relevantes para o exercício da profissão e Autocontrole emocional e Colaboração entre pares como as que mais gostariam de ter apoio na rede para o desenvolvimento.</p>

                    <p class="texto-simples-instituto">Entre os principais achados, também vemos que 96% deles têm orgulho da profissão e mais de 90% sentem-se animados por trabalhar com estudantes e felizes quando se conectam com eles. O levantamento também mostrou que os educadores querem ajudar
                        os estudantes a lidarem com suas emoções e que gostariam de ter apoio para seu desenvolvimento socioemocional, especialmente no autocontrole emocional e na colaboração entre pares.</p>

                    <p class="texto-simples-instituto">O mapeamento busca compreender como os professores identificam suas próprias competências socioemocionais no âmbito escolar, com o objetivo de fortalecer o autoconhecimento e apoiar o desenvolvimento pleno dos profissionais da educação. Mais
                        de 95% concordaram que as 18 competências apresentadas pelo estudo são importantes ou extremamente importantes para as práticas do professor, e mais de 90% dos participantes disseram fazer o seu melhor para ajudar os estudantes a lidar
                        com emoções, reconhecendo a relevância dessa dimensão no ambiente escolar, conforme previsto na BNCC (Base Nacional Comum Curricular).</p>

                    <p class="texto-simples-instituto">Aplicado a partir de uma metodologia de autorrelato e com participação voluntária, o mapeamento também cria oportunidades para a expressão de suas necessidades enquanto profissionais. Quando questionados sobre quais competências eles mais
